package top_programizExamplesPractice_2024_running.examples_02_TypeConversion;

public class Problem_009_02 {
    // Example 3: Java Program to Convert int to String using (+ Operator)
    public static void main(String[] args) {
        int num1 = 25;
        int num2 = 35;
        String str1 = "" + num1; //Here, we are using the string concatenation operation to convert an integer into the string.
        String str2 = "" + num2;
        System.out.println(str1);
        System.out.println(str2);
    }


}
