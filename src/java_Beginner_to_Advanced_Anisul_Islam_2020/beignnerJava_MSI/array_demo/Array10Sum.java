
package java_Beginner_to_Advanced_Anisul_Islam_2020.beignnerJava_MSI.array_demo;

public class Array10Sum {
    public static void main(String[] args) {
        int[] num = {2,3,4,5,6};
        int sum = 0;
        for (int x: num) {
            sum=sum+x;
        }
        System.out.println(sum);
    }
    
}
