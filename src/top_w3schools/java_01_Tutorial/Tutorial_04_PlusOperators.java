package top_w3schools.java_01_Tutorial;

public class Tutorial_04_PlusOperators {
    public static void main(String[] args) {
        int sum1 = 100 + 50;        // 150 (100 + 50)
        int sum2 = sum1 + 250;      // 400 (150 + 250)
        int sum3 = sum2 + sum2;     // 800 (400 + 400)
    }
}

//Although the + operator is often used to add together
//two values, like in the example above,
//it can also be used to add together a variable and a value,
//or a variable and another variable.
