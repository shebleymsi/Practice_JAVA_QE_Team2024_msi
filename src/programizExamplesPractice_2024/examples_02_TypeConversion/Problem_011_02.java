package programizExamplesPractice_2024.examples_02_TypeConversion;


public class Problem_011_02 {
    //Example 3: Java Program to Convert Double to int

    public static void main(String[] args) {

        // create an instance of Double
        Double obj = 78.6;

        // convert obj to int
        // using intValue()
        int num = obj.intValue();

        // print the int value
        System.out.println(num);    // 78
    }
}


