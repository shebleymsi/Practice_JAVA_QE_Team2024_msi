package programizExamplesPractice_2024.examples_02_TypeConversion;


public class Problem_013_02 {
    //Example 3: Java Program to Convert double to String using + Operator
    public static void main(String[] args) {

        // create double variables
        double num1 = 347.6D;
        double num2 = 86.56D;

        // convert double to string
        // using + sign
        String str1 = "" + num1;
        String str2 = "" + num2;

        // print string variables
        System.out.println(str1);    // 347.6
        System.out.println(str2);    // 86.56
    }


}
