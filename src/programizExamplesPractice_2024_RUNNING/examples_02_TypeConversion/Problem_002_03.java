package programizExamplesPractice_2024_RUNNING.examples_02_TypeConversion;



public class Problem_002_03 {
    //Example 4: char to int by subtracting(-) with '0'
    public static void main(String[] args) {

        // create char variables
        char a = '9';
        char b = '3';

        // convert char variables to int
        // by subtracting with char 0
        int num1 = a - '0';
        int num2 = b - '0';

        // print numeric value
        System.out.println(num1);    // 9
        System.out.println(num2);    // 3
    }



}
