package w3schools.java_01_Tutorial;

public class Tutorial_09_even_odd {
    public static void main(String[] args) {
        int myNumber = 5;
        if(myNumber % 2 == 0){
            System.out.println(myNumber + " is odd");
        }else{
            System.out.println(myNumber + " is even");
        }
    }
}
