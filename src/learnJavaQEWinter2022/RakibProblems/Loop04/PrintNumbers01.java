package learnJavaQEWinter2022.RakibProblems.Loop04;

public class PrintNumbers01 {

    public static void main(String[] args) {
        // Declare an integer variable
        int n = 10;

        // Print numbers from 1 to n
        for (int i = 1; i <= n; i++) {
            System.out.print(i + " ");
        }
    }
}
